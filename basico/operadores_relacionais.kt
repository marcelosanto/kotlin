/*
  -> (==) Operador de Igualdade
  -> (<) Operador de Menor
  -> (>) Operador de Maior
  -> (<=) Operador de Menor igual
  -> (>=) Operador de Maior igual
  -> (!=) Operador de Diferente
*/

fun main() {
  var valor1 = 10
  var valor2 = 20

  println(valor1 == valor2)
  println(valor1 < valor2)
  println(valor1 > valor2)
  println(valor1 <= valor2)
  println(valor1 >= valor2)
  println(valor1 != valor2)
}