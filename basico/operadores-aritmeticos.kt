/*
  -> (+) Operador de Soma
  -> (-) Operador de Subtração
  -> (/) Operador de Divisão
  -> (*) Operador de Multiplicação
  -> (%) Modulo - Resto da Divisão
  -> (++) Incremento
  -> (--) Decremento
*/

fun main() {
  var numero1 = 100
  var numero2 = 20

  println(numero1 + numero2)
  println(numero1 - numero2)
  println(numero1 * numero2)
  println(numero1 / numero2)
  println(numero1 % numero2)
  println(++numero1)
  println(--numero2)

  

}